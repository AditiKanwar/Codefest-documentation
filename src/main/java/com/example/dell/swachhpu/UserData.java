package com.example.dell.swachhpu;

public class UserData {
    public String Description;
    public String Department;
    public String Floor;
    public String Status;
    public String Date;
    public String Name;

    public UserData() {
    }

    public UserData(String description, String department, String floor, String status, String date, String name) {
        Description = description;
        Department = department;
        Floor = floor;
        Status = status;
        Date = date;
        Name = name;
    }
}

